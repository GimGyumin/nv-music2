export type TranslationKey = keyof typeof dictionaries.en;

export function translate(
  key: TranslationKey,
  language: AppLanguage,
  values: Record<string, string | number> = {}
) {
  let text: string = dictionaries[language]?.[key] || dictionaries.en[key] || key;
  for (const [name, value] of Object.entries(values)) {
    text = text.replaceAll(`{${name}}`, String(value));
  }
  return text;
}

export function useI18n() {
  const appLanguage = useAuthStore((state) => state.appLanguage || 'en');
  const setAppLanguage = useAuthStore((state) => state.setAppLanguage);

  return {
    language: appLanguage,
    setLanguage: setAppLanguage,
    t: (key: TranslationKey, values?: Record<string, string | number>) =>
      translate(key, appLanguage, values),
  };
}
